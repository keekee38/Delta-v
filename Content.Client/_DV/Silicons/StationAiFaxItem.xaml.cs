using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;

namespace Content.Client._DV.Silicons;

[GenerateTypedNameReferences]
public sealed partial class StationAiFaxItem : PanelContainer
{
    public string? Text { get => PaperName.Text; set => PaperName.Text = value; }
    public bool CanSend { get => !Duplicate.Disabled; set => Duplicate.Disabled = !value; }

    public event Action? ShredPressed;
    public event Action? DuplicatePressed;
    public event Action? ExaminePressed;
    public event Action? SendPressed;

    public StationAiFaxItem()
    {
        RobustXamlLoader.Load(this);

        Shred.OnPressed += _ => ShredPressed?.Invoke();
        Duplicate.OnPressed += _ => DuplicatePressed?.Invoke();
        Examine.OnPressed += _ => ExaminePressed?.Invoke();
        Send.OnPressed += _ => SendPressed?.Invoke();
    }
}
