using Content.Client.UserInterface.Controls;
using Content.Shared.Fax;
using Content.Shared.Labels.Components;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.XAML;

namespace Content.Client._DV.Silicons;

[GenerateTypedNameReferences]
public sealed partial class StationAiFaxWindow : FancyWindow
{
    [Dependency] private readonly IEntityManager _entityManager = default!;

    public event Action? BlankButtonPressed;
    public event Action? FileButtonPressed;
    public event Action<EntityUid>? OpenSendWindowPressed;
    public event Action<EntityUid>? ExaminePressed;
    public event Action<EntityUid>? ShredPressed;
    public event Action<EntityUid>? DuplicatePressed;

    public bool CanSend
    {
        get;
        set
        {
            if (field == value)
                return;

            field = value;
            foreach (var item in FaxList.Children)
            {
                var castItem = (StationAiFaxItem)item;
                castItem.CanSend = value;
            }

            NoFaxesBlank.Disabled = !value;
            NoFaxesUpload.Disabled = !value;
            YesFaxesBlank.Disabled = !value;
            YesFaxesUpload.Disabled = !value;
        }
    }

    public StationAiFaxWindow()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);

        NoFaxesBlank.OnPressed += _ => BlankButtonPressed?.Invoke();
        NoFaxesUpload.OnPressed +=  _ => FileButtonPressed?.Invoke();

        YesFaxesBlank.OnPressed += _ => BlankButtonPressed?.Invoke();
        YesFaxesUpload.OnPressed +=  _ => FileButtonPressed?.Invoke();
    }

    public void UpdateFaxes(Robust.Shared.Containers.Container container)
    {
        FaxList.RemoveAllChildren();
        foreach (var item in container.ContainedEntities)
        {
            var page = new StationAiFaxItem
            {
                Text = _entityManager.TryGetComponent<LabelComponent>(item, out var label) &&
                       label.CurrentLabel is { } labelText
                    ? labelText
                    : _entityManager.GetComponent<MetaDataComponent>(item).EntityName,
                CanSend = CanSend,
            };
            FaxList.AddChild(page);

            page.ExaminePressed += () => ExaminePressed?.Invoke(item);
            page.ShredPressed += () => ShredPressed?.Invoke(item);
            page.DuplicatePressed += () => DuplicatePressed?.Invoke(item);
            page.SendPressed += () => OpenSendWindowPressed?.Invoke(item);
        }

        if (container.ContainedEntities.Count == 0)
        {
            YesFaxes.Visible = false;
            NoFaxes.Visible = true;
        }
        else
        {
            YesFaxes.Visible = true;
            NoFaxes.Visible = false;
        }
    }

    public void UpdateState(FaxUiState state)
    {
        CanSend = state.CanSendCooldown;
    }
}
